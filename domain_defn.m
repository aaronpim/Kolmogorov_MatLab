function [model] = domain_defn(v)
model = createpde(1);
gd = ...
[3, 3, 3, 3;...
 4, 4, 4, 4;...
 0, 0,-1,-1;...
 1, 1, 0, 0;...
 1, 1, 0, 0;...
 0, 0,-1,-1;...
 0,-v, 0,-v;...
 0,-v, 0,-v;...
 v, 0, v, 0;...
 v, 0, v, 0];
ns = [82, 82, 82, 82; 49, 50, 51, 52];
sf = 'R1+R2+R3+R4';
dl = decsg(gd,sf,ns);
geometryFromEdges(model,dl);
applyBoundaryCondition(model,"dirichlet","edge",[3,4,5,6],"u",0);
end